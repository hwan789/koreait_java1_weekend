package com.kita.first.level2;

public class For1 {
public static void main(String[] args) {

//	for(초기식; 조건식; 증감식) {
//	        실행식
//	}
	for(int i = 0; i<10; i++) {
		System.out.println(i+"입니다.");	
	}
	
	for(int i = 1; i<=5; i++) {
		System.out.println(i+"회 반복했습니다.");
	}
	
  
 }
}
